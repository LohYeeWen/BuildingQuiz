package com.example.building_quiz;

public class Score {
    private int id;
    private int score;

    public Score() {
    }

    public Score(int score) {
        this.score = score;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }
}
